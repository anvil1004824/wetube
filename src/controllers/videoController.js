export const postUpload = async (req, res) => {
  const {
    user: { _id },
  } = req.session;
  const { video, thumb } = req.files;
  const { title, description, hashtags } = req.body;
  const isHeroku = process.env.NODE_ENV === "production";
  try {
    const newVideo = await Video.create({
      title,
      description,
      fileUrl: isHeroku ? video[0].location : "/" + video[0].path,
      thumbUrl: isHeroku ? thumb[0].location : "/" + thumb[0].path,
      owner: _id,
      hashtags: Video.formatHashtags(hashtags),
    });
    const user = await User.findById(_id);
    user.videos.push(newVideo._id);
    await user.save();
    return res.redirect("/");
  } catch (error) {
    return res.status(400).render("upload", {
      pageTitle: "Upload Video",
      errorMessage: error._message,
    });
  }
};

export const deleteVideo = async (req, res) => {
  const { id } = req.params;
  const {
    user: { _id },
  } = req.session;
  const video = await Video.findById(id);
  const user = await User.findById(video.owner);
  if (!video || !user) {
    return res.status(404).render("404", { pageTitle: "Video not found." });
  }
  if (String(video.owner) !== _id) {
    return res.status(403).redirect("/");
  }
  s3.deleteObject(
    {
      Bucket: "wetube-anvil",
      Key: `videos/${video.fileUrl.split("/")[4]}`,
    },
    (err, data) => {
      if (err) {
        console.log(err);
      } else {
        console.log("DELETE SUCCESS");
      }
    }
  );
  s3.deleteObject(
    {
      Bucket: "wetube-anvil",
      Key: `videos/${video.thumbUrl.split("/")[4]}`,
    },
    (err, data) => {
      if (err) {
        console.log(err);
      } else {
        console.log("DELETE SUCCESS");
      }
    }
  );
  user.videos = user.videos.filter((video) => String(video) !== id);
  await video.comments.forEach(async (item) => {
    await Comment.findByIdAndDelete(item);
  });
  await Video.findByIdAndDelete(id);
  await user.save();
  return res.redirect("/");
};

export const search = async (req, res) => {
  const { keyword } = req.query;
  let videos = [];
  if (keyword) {
    videos = await Video.find({
      title: {
        $regex: new RegExp(keyword, "i"),
      },
    }).populate("owner");
  }
  return res.render("search", { pageTitle: "Search", videos });
};

export const registerView = async (req, res) => {
  const { id } = req.params;
  const video = await Video.findById(id);
  if (!video) {
    return res.sendStatus(404);
  }
  video.meta.views = video.meta.views + 1;
  await video.save();
  return res.sendStatus(200);
};

export const createComment = async (req, res) => {
  const {
    params: { id },
    body: { text },
    session: { user },
  } = req;
  const video = await Video.findById(id);
  if (!video) {
    return res.sendStatus(404);
  }
  const comment = await Comment.create({
    text,
    owner: user._id,
    video: id,
  });
  video.comments.push(comment._id);
  await video.save();
  return res.status(201).json({ newCommentId: comment._id });
};

export const deleteComment = async (req, res) => {
  const {
    params: { id },
    session: {
      user: { _id },
    },
  } = req;
  const comment = await Comment.findById(id);
  const video = await Video.findById(comment.video);
  if (!video || !comment) {
    return res.sendStatus(404);
  }
  if (String(comment.owner) !== _id) {
    return res.sendStatus(403);
  }
  video.comments = video.comments.filter((item) => String(item) !== id);
  await video.save();
  await Comment.findByIdAndDelete(id);
  return res.sendStatus(200);
};
